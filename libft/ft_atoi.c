#include "libft.h"

int				ft_atoi(const char *str)
{
	int		i;
	int		sign;
	long	rev;

	i = 0;
	sign = 1;
	rev = 0;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
			|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while ('0' <= str[i] && str[i] <= '9')
	{
		rev = rev * 10 + (str[i] - '0');
		i++;
	}
	return ((int)sign * rev);
}
